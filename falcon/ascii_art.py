def render_ascii_number(n):
    formatted = f"{n:,}"
    lines = [""] * 6
    for ch in formatted:
        if ch in DIGITS:
            for i in range(6):
                lines[i] += DIGITS[ch][i] + " "
    return "\n".join("  " + line for line in lines)
