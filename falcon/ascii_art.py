def render_ascii_number(n):
    formatted = f"{n:,}"
    lines = [""] * 6
    for ch in formatted:
        if ch in DIGITS:
            for i in range(6):
                lines[i] += DIGITS[ch][i] + " "
    return "\n".join("  " + line for line in lines)


def render_small_number(n):
    """Render a number using compact 3-line digits."""
    formatted = f"{n:,}"
    lines = ["", "", ""]
    for ch in formatted:
        if ch in SMALL_DIGITS:
            for i in range(3):
                lines[i] += SMALL_DIGITS[ch][i] + " "
    return "\n".join(lines)
